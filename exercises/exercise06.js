// Assignment operators
let priceKeyboard = 100;

console.log(priceKeyboard);
priceKeyboard += priceKeyboard;
console.log(priceKeyboard);
priceKeyboard -= priceKeyboard;
console.log(priceKeyboard);
priceKeyboard *= priceKeyboard;
console.log(priceKeyboard);
priceKeyboard /= priceKeyboard;
console.log(priceKeyboard);

// Equality operators
console.log(1 === 1);
console.log("1" === 1);
console.log("1" == 1);
